import math


# Задаем значения a и b
a = 5.0
b = 10.0

# Вычисляем среднее арифметическое
arithmetic_average = (a + b) / 2

# Вычисляем среднее геометрическое
geometric_mean = math.sqrt(a * b)

# Выводим результаты
print("Среднее арифметическое:", arithmetic_average)
print("Среднее геометрическое:", geometric_mean)
